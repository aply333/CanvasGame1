function matrixLoop(arr, ret, callback) {
  let newArr = [];
  for (let y = 0; y < arr.length; y++) {
    let rowX = [];
    if (ret === true) {
      for (let x = 0; x < arr[y].length; x++) {
        rowX.push(callback(arr, y, x));
      }
      newArr.push(rowX);
    } else {
      for (let x = 0; x < arr[y].length; x++) {
        callback(arr, y, x);
      }
    }
  }
  return newArr;
}

